_SUBPROCESS_RUNNER = textwrap.dedent('''
    """Subprocess-side runner — invoked by ``SubprocessSandbox``.

    Reads a JSON payload on stdin with keys ``patched_source`` /
    ``test_source`` / ``module_name``, executes them under the
    Phase-39 contract, prints a single JSON line on stdout, and
    exits 0. Any failure is captured and reported via the JSON
    payload, not via process exit code, so the parent can
    distinguish *test-level* failures from *sandbox-level*
    failures.
    """
    import io, json, sys, traceback
    from contextlib import redirect_stdout, redirect_stderr

    def _emit(payload):
        sys.__stdout__.write(json.dumps(payload) + "\\n")
        sys.__stdout__.flush()

    try:
        req = json.loads(sys.stdin.read())
    except Exception as ex:
        _emit({"ok": False, "kind": "sandbox_error",
               "detail": f"input parse failed: {type(ex).__name__}: {ex}"})
        sys.exit(0)

    patched_source = req["patched_source"]
    test_source = req["test_source"]
    module_name = req.get("module_name", "patched_under_test")

    # Compile patched source.
    try:
        code = compile(patched_source, f"<{module_name}>", "exec")
    except SyntaxError as ex:
        _emit({"ok": False, "kind": "syntax",
               "detail": f"{type(ex).__name__}: {ex}"})
        sys.exit(0)
    mod_globals = {"__name__": module_name, "__doc__": None,
                   "__builtins__": __builtins__}
    try:
        with redirect_stdout(io.StringIO()), redirect_stderr(io.StringIO()):
            exec(code, mod_globals)
    except Exception as ex:
        _emit({"ok": False, "kind": "import",
               "detail": f"{type(ex).__name__}: {ex}"})
        sys.exit(0)

    # Compile + exec test source.
    try:
        test_code = compile(test_source, "<patched_test>", "exec")
        test_globals = {"__name__": "<patched_test>", "__doc__": None,
                        "__builtins__": __builtins__}
        exec(test_code, test_globals)
    except SyntaxError as ex:
        _emit({"ok": False, "kind": "syntax",
               "detail": f"test syntax: {ex}"})
        sys.exit(0)
    test_fn = test_globals.get("test")
    if not callable(test_fn):
        _emit({"ok": False, "kind": "test_exception",
               "detail": "test source did not define test(module)"})
        sys.exit(0)
    class _Mod: pass
    mod_obj = _Mod()
    for k, v in mod_globals.items():
        if not k.startswith("__"):
            setattr(mod_obj, k, v)
    try:
        with redirect_stdout(io.StringIO()), redirect_stderr(io.StringIO()):
            test_fn(mod_obj)
    except AssertionError as ex:
        _emit({"ok": False, "kind": "test_assert",
               "detail": str(ex)[:300]})
        sys.exit(0)
    except Exception as ex:
        _emit({"ok": False, "kind": "test_exception",
               "detail": f"{type(ex).__name__}: {ex}"})
        sys.exit(0)
    _emit({"ok": True, "kind": "", "detail": ""})
''')


@dataclass
class SubprocessSandbox:
    """Run patched + test source in a fresh ``python`` subprocess.

    Each ``run`` call:

      * writes a temp directory containing the runner script;
      * spawns ``python <runner>`` with the patch payload on
        stdin;
      * enforces ``timeout_s`` via ``subprocess.run(timeout=...)``;
      * captures stdout / stderr bytes;
      * parses the runner's single JSON line back into a
        ``WorkspaceResult``.

    The subprocess is started with ``stdin=PIPE``, ``stdout=PIPE``,
    ``stderr=PIPE``, no inherited file descriptors beyond those
    three, and ``cwd=<tempdir>``. On Unix, ``preexec_fn=os.setsid``
    starts a new process group so a ``timeout`` triggers
    ``Popen.kill()`` against the whole group rather than just the
    leader.
    """

    python_executable: str = field(default_factory=lambda: sys.executable)
    pass_environ: bool = False  # if False, child gets a minimal env

    def name(self) -> str:
        return "subprocess"

    def is_available(self) -> bool:
        return os.path.exists(self.python_executable)

    def run(self,
            *,
            buggy_source: str,
            patch: tuple[tuple[str, str], ...],
            test_source: str,
            module_name: str,
            timeout_s: float = 30.0,
            apply_mode: str = APPLY_MODE_STRICT,
            ) -> WorkspaceResult:
        new_source, applied, reason = apply_patch(
            buggy_source, patch, mode=apply_mode)
        if not applied:
            return WorkspaceResult(
                patch_applied=False, syntax_ok=False,
                test_passed=False, error_kind="patch_no_match",
                error_detail=reason)
        with tempfile.TemporaryDirectory(prefix="ctxzero_swe_") as tmp:
            runner_path = os.path.join(tmp, "_runner.py")
            with open(runner_path, "w", encoding="utf-8") as fh:
                fh.write(_SUBPROCESS_RUNNER)
            payload = json.dumps({
                "patched_source": new_source,
                "test_source": test_source,
                "module_name": module_name,
            }).encode("utf-8")
            env = (os.environ.copy() if self.pass_environ
                    else {"PATH": os.environ.get("PATH", ""),
                          "PYTHONHASHSEED": "0",
                          "PYTHONIOENCODING": "utf-8",
                          "LANG": "C.UTF-8",
                          "PYTHONDONTWRITEBYTECODE": "1"})
            t0 = time.time()
            try:
                proc = subprocess.run(
                    [self.python_executable, "-S", "-B", runner_path],
                    input=payload, capture_output=True,
                    timeout=timeout_s, cwd=tmp, env=env, check=False)
            except subprocess.TimeoutExpired as ex:
                return WorkspaceResult(
                    patch_applied=True, syntax_ok=True,
                    test_passed=False, error_kind="timeout",
                    error_detail=(
                        f"subprocess exceeded timeout_s={timeout_s} "
                        f"(wall={time.time() - t0:.2f}s); "
                        f"stdout_chars={len(ex.stdout or b'')} "
                        f"stderr_chars={len(ex.stderr or b'')}"))
            except (OSError, ValueError) as ex:
                return WorkspaceResult(
                    patch_applied=True, syntax_ok=False,
                    test_passed=False, error_kind="sandbox_error",
                    error_detail=f"spawn failed: {type(ex).__name__}: {ex}")
            return _parse_runner_payload(proc, new_source)


def _parse_runner_payload(proc: subprocess.CompletedProcess,
                            patched_source: str) -> WorkspaceResult:
    """Convert the subprocess runner's stdout into a WorkspaceResult.

    Handles three failure shapes: (a) the runner emitted a clean
    JSON outcome (the common path); (b) the runner crashed before
    emitting (returncode != 0, stdout empty); (c) the runner
    emitted partial / malformed output (capture as
    ``sandbox_error`` so the failure is attributable to the
    boundary, not to the patch).
    """
    stdout = (proc.stdout or b"").decode("utf-8", errors="replace").strip()
    stderr = (proc.stderr or b"").decode("utf-8", errors="replace")
    # Take the last non-empty stdout line — the runner emits
    # exactly one JSON line on a clean run.
    last = ""
    for line in stdout.splitlines():
        if line.strip():
            last = line.strip()
    if not last:
        return WorkspaceResult(
            patch_applied=True, syntax_ok=bool(patched_source),
            test_passed=False, error_kind="sandbox_error",
            error_detail=(
                f"runner produced no JSON output; "
                f"returncode={proc.returncode}; "
                f"stderr_head={stderr[:200]!r}"))
    try:
        outcome = json.loads(last)
    except json.JSONDecodeError as ex:
        return WorkspaceResult(
            patch_applied=True, syntax_ok=True,
            test_passed=False, error_kind="sandbox_error",
            error_detail=(
                f"runner JSON parse failed: {ex}; "
                f"last_line={last[:200]!r}"))
    if not isinstance(outcome, dict):
        return WorkspaceResult(
            patch_applied=True, syntax_ok=True,
            test_passed=False, error_kind="sandbox_error",
            error_detail=f"runner outcome not a dict: {type(outcome).__name__}")
    ok = bool(outcome.get("ok", False))
    kind = str(outcome.get("kind", ""))
    detail = str(outcome.get("detail", ""))[:300]
    if ok:
        return WorkspaceResult(
            patch_applied=True, syntax_ok=True,
            test_passed=True, error_kind="", error_detail="")
    syntax_ok = kind not in ("syntax",)
    return WorkspaceResult(
        patch_applied=True, syntax_ok=syntax_ok,
        test_passed=False, error_kind=kind or "sandbox_error",
        error_detail=detail)


