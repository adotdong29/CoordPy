# =============================================================================
# Sandbox protocol
# =============================================================================


class Sandbox(Protocol):
    """Common interface for all execution backends.

    Implementations must:

      1. Apply ``patch`` to ``buggy_source`` (string substitutions).
      2. Compile the patched source.
      3. Load it as a module.
      4. Execute ``test_source`` against it; the test must
         define ``def test(module) -> None`` (the Phase-39
         contract).
      5. Return a ``WorkspaceResult`` whose ``error_kind`` is
         one of the canonical Phase-39 kinds, plus the new
         ``"timeout"`` and ``"sandbox_error"`` kinds.
    """

    def name(self) -> str: ...

    def run(self,
            *,
            buggy_source: str,
            patch: tuple[tuple[str, str], ...],
            test_source: str,
            module_name: str,
            timeout_s: float,
            apply_mode: str = APPLY_MODE_STRICT,
            ) -> WorkspaceResult: ...

    def is_available(self) -> bool: ...


