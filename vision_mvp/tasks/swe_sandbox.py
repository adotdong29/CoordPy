@dataclass
class InProcessSandbox:
    """Phase-39 in-process exec wrapped into the Phase-40 protocol.

    Provided so a driver can set ``--sandbox=in_process`` to
    reproduce Phase-39 numbers exactly (and to keep a fast
    regression baseline).
    """

    def name(self) -> str:
        return "in_process"

    def is_available(self) -> bool:
        return True

    def run(self,
            *,
            buggy_source: str,
            patch: tuple[tuple[str, str], ...],
            test_source: str,
            module_name: str,
            timeout_s: float = 30.0,
            apply_mode: str = APPLY_MODE_STRICT,
            ) -> WorkspaceResult:
        new_source, applied, reason = apply_patch(
            buggy_source, patch, mode=apply_mode)
        if not applied:
            return WorkspaceResult(
                patch_applied=False, syntax_ok=False,
                test_passed=False, error_kind="patch_no_match",
                error_detail=reason)
        # The Phase-39 in-process runner ignores ``timeout_s``
        # because there is no clean way to interrupt arbitrary
        # Python from a single thread. Honest reporting only.
        del timeout_s
        return run_patched_test(
            file_source=buggy_source, patched_source=new_source,
            test_source=test_source, module_name=module_name)


