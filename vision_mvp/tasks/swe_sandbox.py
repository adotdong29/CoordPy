@dataclass
class DockerSandbox:
    """Run the patch+test cycle inside a short-lived Docker container.

    Each ``run`` materialises a tempdir on the host containing the
    runner script and the JSON payload, then invokes::

        docker run --rm \\
            --network=none \\
            --read-only \\
            --tmpfs /work:rw,size=64m,mode=1777 \\
            -v <host_tmp>:/work/in:ro \\
            <image> python /work/in/_runner.py < /work/in/_payload.json

    The container has no network, no writable rootfs, no
    inherited environment beyond what the image specifies, and a
    wall-clock kill via ``--stop-timeout``. The runner script is
    the same one ``SubprocessSandbox`` uses, so the parsed
    ``WorkspaceResult`` shape is identical.

    Docker is *optional*. ``is_available`` returns False if the
    daemon is unreachable; the driver falls back to
    ``SubprocessSandbox``.
    """

    image: str = "python:3.11-slim"
    docker_bin: str = "docker"

    def name(self) -> str:
        return "docker"

    def is_available(self) -> bool:
        bin_path = shutil.which(self.docker_bin)
        if not bin_path:
            return False
        try:
            r = subprocess.run(
                [bin_path, "info", "--format", "{{.ServerVersion}}"],
                capture_output=True, timeout=4.0, check=False)
            return r.returncode == 0
        except Exception:
            return False

    def run(self,
            *,
            buggy_source: str,
            patch: tuple[tuple[str, str], ...],
            test_source: str,
            module_name: str,
            timeout_s: float = 60.0,
            apply_mode: str = APPLY_MODE_STRICT,
            ) -> WorkspaceResult:
        new_source, applied, reason = apply_patch(
            buggy_source, patch, mode=apply_mode)
        if not applied:
            return WorkspaceResult(
                patch_applied=False, syntax_ok=False,
                test_passed=False, error_kind="patch_no_match",
                error_detail=reason)
        if not self.is_available():
            return WorkspaceResult(
                patch_applied=True, syntax_ok=False,
                test_passed=False, error_kind="sandbox_error",
                error_detail="docker not available; use a different backend")
        with tempfile.TemporaryDirectory(prefix="ctxzero_dock_") as tmp:
            runner_path = os.path.join(tmp, "_runner.py")
            payload_path = os.path.join(tmp, "_payload.json")
            with open(runner_path, "w", encoding="utf-8") as fh:
                fh.write(_SUBPROCESS_RUNNER)
            with open(payload_path, "w", encoding="utf-8") as fh:
                json.dump({
                    "patched_source": new_source,
                    "test_source": test_source,
                    "module_name": module_name,
                }, fh)
            cmd = [
                self.docker_bin, "run", "--rm",
                "--network=none", "--read-only",
                "--tmpfs", "/work:rw,size=64m,mode=1777",
                "-v", f"{tmp}:/work/in:ro",
                "-w", "/work",
                "--stop-timeout", str(int(timeout_s) + 2),
                self.image,
                "sh", "-c",
                "python /work/in/_runner.py < /work/in/_payload.json",
            ]
            t0 = time.time()
            try:
                proc = subprocess.run(
                    cmd, capture_output=True,
                    timeout=timeout_s + 5.0, check=False)
            except subprocess.TimeoutExpired as ex:
                return WorkspaceResult(
                    patch_applied=True, syntax_ok=True,
                    test_passed=False, error_kind="timeout",
                    error_detail=(
                        f"docker run exceeded timeout_s={timeout_s} "
                        f"(wall={time.time() - t0:.2f}s); "
                        f"stderr_head={(ex.stderr or b'')[:200]!r}"))
            except FileNotFoundError as ex:
                return WorkspaceResult(
                    patch_applied=True, syntax_ok=False,
                    test_passed=False, error_kind="sandbox_error",
                    error_detail=f"docker binary not found: {ex}")
            return _parse_runner_payload(proc, new_source)


