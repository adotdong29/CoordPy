# Sandbox factory
# =============================================================================


def select_sandbox(name: str = "auto",
                    *,
                    docker_image: str = "python:3.11-slim",
                    ) -> Sandbox:
    """Return the requested sandbox backend.

    ``name`` ∈ {``"in_process"``, ``"subprocess"``, ``"docker"``,
    ``"auto"``}. ``"auto"`` prefers Docker → subprocess →
    in_process based on availability. The driver records the
    selected backend's ``name()`` in the result artifact so the
    measurement is fully attributable.
    """
    if name == "in_process":
        return InProcessSandbox()
    if name == "subprocess":
        return SubprocessSandbox()
    if name == "docker":
        return DockerSandbox(image=docker_image)
    if name == "auto":
        d = DockerSandbox(image=docker_image)
        if d.is_available():
            return d
        s = SubprocessSandbox()
        if s.is_available():
            return s
        return InProcessSandbox()
    raise ValueError(f"unknown sandbox name {name!r}")


