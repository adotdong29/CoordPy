"""Phase 40 — sandboxed patch+test execution boundary.

Phase 39's ``run_patched_test`` executes the patched module and the
hidden test in-process via ``exec`` against a fresh ``__main__``-
shaped dict. That is safe for the *self-authored* mini bank but
not for arbitrary patches generated by an external LLM against
arbitrary code: a malicious or merely buggy patch can ``import
os; os.system(...)`` straight into the bridge process.

Phase 40 adds a real execution boundary:

  * **``SubprocessSandbox``** — run the same compile+exec
    cycle inside a fresh ``python -c`` subprocess, with the
    patched source written to a tempdir, the test source
    importing it as a module, a wall-clock timeout, no stdin,
    and stdout/stderr captured. The subprocess inherits the
    bridge process's interpreter and site-packages; it does
    NOT inherit the bridge's open file descriptors, signal
    handlers, or process state. This is *isolation by
    process boundary*, not *isolation by capability*.

  * **``DockerSandbox``** — if a local Docker daemon is
    available, run the same cycle inside a short-lived
    container with ``--network=none``, ``--read-only``
    rootfs, a writable ``/work`` tmpfs, and a wall-clock
    timeout. The container image is configurable; the
    default (``python:3.11-slim``) is enough for the
    Phase-40 mini-bank smoke run. Docker is optional —
    ``DockerSandbox.is_available()`` returns False when
    no daemon is reachable, and the driver falls back to
    ``SubprocessSandbox``.

  * **``InProcessSandbox``** — wraps the existing Phase-39
    ``run_patched_test`` so the three backends share one
    ``Sandbox`` interface. Useful as a regression baseline
    and for the mini-bank precondition tests.

What is and is NOT isolated
---------------------------

| Property                              | InProcess | Subprocess | Docker  |
|---                                    |---        |---         |---      |
| Crash containment                     | no        | yes        | yes     |
| Wall-clock timeout                    | no        | yes        | yes     |
| Memory bound (heap)                   | no        | soft (rlimit) | yes (cgroup)  |
| Filesystem isolation                  | no        | tempdir-only | tmpfs read-only rootfs  |
| Network isolation                     | no        | no         | yes (--network=none)  |
| Capability/syscall isolation          | no        | no         | partial (default seccomp)  |
| Side-effect rollback on test failure  | no        | yes        | yes     |
| Untrusted-code safety                 | NO        | partial    | yes (modulo Docker CVEs)  |

The honest summary: **``SubprocessSandbox`` is the right
default for Phase 40**. It contains crashes, enforces wall-
clock and (best-effort) memory bounds, and isolates the
filesystem to a tempdir — which is enough to credibly run
the mini-bank against an LLM-generated patch without
trusting the patch. ``DockerSandbox`` is the right choice
for an external-corpus run; it is implemented but not
required for the Phase-40 mini-bank measurements.

What this module does NOT claim
-------------------------------

* ``SubprocessSandbox`` does NOT block arbitrary network
  egress. A patch that issues an outbound HTTPS request can
  still reach the network from the subprocess. The right
  remediation is the Docker backend.
* ``SubprocessSandbox`` does NOT prevent the patch from
  reading the host's filesystem outside the tempdir; only
  *writes* are sandboxed by the tempdir convention. Use
  Docker when read-side isolation matters.
* ``DockerSandbox`` inherits Docker's own threat model.
  Container escape via daemon CVEs is out of scope; this
  module is a research-grade boundary, not a production-
  grade sandbox.
* The substrate test grader (``run_patched_test``'s
  ``def test(module): ...`` contract) is unchanged. Tests
  that do not match this contract are the loader's problem,
  not the sandbox's.

Theoretical anchor: RESULTS_PHASE40.md § B.3 (Theorem
P40-3 — sandbox-boundary preservation: every Phase-39
oracle-ceiling result holds under every Phase-40 sandbox
backend; empirical equivalence on the mini bank).
"""

